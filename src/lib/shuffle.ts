/* --- BUG-005 / BUG-006: Fisher-Yates shuffle (Math.random()-0.5 biased sort yerine) --- */
export function fisherYates<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}
